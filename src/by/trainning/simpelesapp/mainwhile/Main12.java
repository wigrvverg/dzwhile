package by.trainning.simpelesapp.mainwhile;

import java.util.Scanner;

public class Main12 {

	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		System.out.print(">");
		int a = scanner.nextInt();
		while(a>0) {
			a--;
			System.out.println(a);
		}

	}

}
